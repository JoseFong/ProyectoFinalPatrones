    private void registrarLibroBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarLibroBtnActionPerformed
        //Extracción de información de cada input o select
        String isbn = isbnInput.getText().trim();
        String tipo = tipoSelect.getSelectedItem().toString();
        String titulo = tituloInput.getText().trim();
        String autor = autorInput.getText().trim();
        String anio = anioInput.getText().trim();
        String genero = generoInput.getText().trim();
        
        //Validación para que se completen todos los campos
        if(isbn.equals("") || titulo.equals("") || genero.equals("") || autor.equals("") || anio.equals("")){
            mensajeErrorLbl.setText("Complete todos los campos.");
        }else{
            //Validación para que ningun dato contenga ";"
            if(isbn.contains(";") || titulo.contains(";") || autor.contains(";") || tipo.contains(";") || genero.contains(";") || anio.contains(";")){
                mensajeErrorLbl.setText("No puede usar ; en ninguno de los campos.");
            }else{
                //Se obtiene la instancia UNICA del gestor de libros y se agrega el libro
                GestorLibros gestor = GestorLibros.getInstancia();
                
                //Se agrega el libro a la lista de libros y al archivo
                gestor.agregarLibro(isbn, tipo, titulo, autor, anio, genero);
                gestor.agregarLibroAlArchivo(isbn, tipo, titulo, autor, anio, genero);
                
                //Se reinician los inputs
                isbnInput.setText("");
                tituloInput.setText("");
                generoInput.setText("");
                autorInput.setText("");
                anioInput.setText("");
                
                //Se muestra un mensaje de éxito.
                mensajeErrorLbl.setText("Libro '"+titulo+"' de '"+autor+"' registrado correctamente.");
            
                //Se actualiza la tabla
                llenarTablaConLibros();
            }   
        }
    }//GEN-LAST:event_registrarLibroBtnActionPerformed

    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        if(busquedaActiva){
            //Se elimina el valor de la busqueda
            valorBusquedaInput.setText("");
            
            //Se llena la tabla con todos los libros
            llenarTablaConLibros();
            
            //Se regresa el botón de buscar y se desactiva busqueda activa
            busquedaActiva = false;
            buscarBtn.setText("Buscar");
            
        }else{
            //Se extraen criterio y valor de busqueda
            String criterio = criterioSelect.getSelectedItem().toString().trim().toLowerCase();
            String valor = valorBusquedaInput.getText().trim().toLowerCase();

            if(criterio.equals("") || valor.equals("")){
                System.out.println("Error al realizar la busqueda.");
            }else{
                //Crear los buscadores
                BuscadorLibro buscadorAutor = new BusquedaPorAutor();
                BuscadorLibro buscadorTitulo = new BusquedaPorTitulo();
                BuscadorLibro buscadorISBN = new BusquedaPorISBN();

                //Encadenar los buscadores
                buscadorAutor.setSiguiente(buscadorTitulo);
                buscadorTitulo.setSiguiente(buscadorISBN);

                //Buscar los libros por el criterio y valor
                List<Libro> resultados = buscadorAutor.buscar(criterio,valor);
                llenarTablaConResultados(resultados);
                
                //Se cambia el texto del botón y el estado de busqueda activa
                buscarBtn.setText("Eliminar criterios de búsqueda");
                busquedaActiva = true;
            }
        }
    }//GEN-LAST:event_buscarBtnActionPerformed

