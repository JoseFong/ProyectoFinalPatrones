    private void llenarTablaConLibros(){
        //Se obitene la instancia del gestor y se obtienen todos los libros en la lista.
        GestorLibros gestor = GestorLibros.getInstancia();
        List<Libro> libros = gestor.getLibros();
        
        DefaultTableModel modelo = (DefaultTableModel) tablaLibros.getModel();
        
        modelo.setRowCount(0);
        
        for(Libro libro: libros){
            Object[] fila = new Object[]{
                libro.getISBN(),
                libro.getTipo(),
                libro.getTitulo(),
                libro.getAutor(),
                libro.getAnio(),
                libro.getGenero()
            };
            modelo.addRow(fila);
        }
    }

